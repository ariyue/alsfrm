package com.als.frm.ex;

import java.io.Serializable;


/**
 * 事务处理异常
 * @author Administrator
 *
 */
public class DBTransException extends DaoException implements Serializable{

	private static final long serialVersionUID = 1L;
	public DBTransException(){
		super();
	}
	
	public DBTransException(String msg){
		super(msg);		
	}
	
	public DBTransException(String msg, Throwable cause){
		super(msg, cause);		
	}
	
	public DBTransException(String msg, String code){
		super(msg, code);		
	}
	
	public DBTransException(String msg, String code, Throwable cause){
		super(msg, code, cause);		
	}
	
	public DBTransException(String msg, String code, String userId){
		super(msg, code, userId);		
	}
	
	public DBTransException(String msg, String code, String userId, Throwable cause){
		super(msg, code, userId, cause);	
	}
}
